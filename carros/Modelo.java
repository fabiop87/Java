package veiculos;

public class Modelo {
    
    
    public String NomeModelo;
    public int PrecoModelo;

    public Modelo(String NomeModelo, int PrecoModelo) {
        this.NomeModelo = NomeModelo;
        this.PrecoModelo = PrecoModelo;
    }

    
    
    public String getNomeModelo() {
        return NomeModelo;
    }

    public void setNomeModelo(String NomeModelo) {
        this.NomeModelo = NomeModelo;
    }

    public int getPrecoModelo() {
        return PrecoModelo;
    }

    public void setPrecoModelo(int PrecoModelo) {
        this.PrecoModelo = PrecoModelo;
    }
    

    
    
 
    
    
    
}
